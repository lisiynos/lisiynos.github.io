// Подключаем стандарные библиотеки
#include <stdio.h>
#include <conio.h>

// ---=== Пример описания функции ===--- //
// Функция вычисляет факториалы чисел от 1 до 13
// Ограничение 13 связано с быстрым возрастанием факториала
long int fact( int number )
// Возврашаемый результат - число типа long int (длинное целое)
// Параметр - целое число (его факториал мы и считаем)
{
  long int Result = 1; // В этой переменной мы вычисляем результат
  // Цикл от 1 до number
  for (int i = 1; i <= number; i++ ) {
    Result *= i; // Умножаем Result на все i из промежутка 1..number
  }
  // Возврашаем результат
  return Result;
};

// ---=== Основная программа ===--- //
void main() {
  // Выводим на экран заголовок программы ("\n"-перевод строки)
  printf("---=== Программа вычисления факториала ===---\n");
  // Выводим приглашение на ввод числа в диапазоне от 1 до 13
  printf("Введите число (1<=n<=13) ");
  // Переменная для хранения вводимого числа
  int i;
  // Вводим число с клавиатуры
  scanf("%d", &i);
  // Проверям попадания числа в заданный промежуток
  if ((i >= 1) && (i <= 13)) { // Вывод значения факториала на экран
    printf("Факториал %d равен %ld\n", i, fact(i));
  } else
    // Выводим сообщение об ошибке на экран
    printf("Число не попадает в заданные рамки (1<=n<=13)\n");
}

